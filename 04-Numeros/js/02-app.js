const numero1 = 30;
const numero2 = 20;


let resultado;

console.log(`Primer valor ${numero1} segundo valor ${numero2}`)

//  Operación Suma

resultado = numero1 + numero2;
console.log(`El resultado de la suma de los dos valores es ${resultado}`);

//  Operación Resta

resultado = numero1 - numero2;
console.log(`El resultado de la resta de los dos valores es ${resultado}`);
//  Operación Multiplicación

resultado = numero1 * numero2;
console.log(`El resultado de la multiplicación de los dos valores es ${resultado}`);

//  Operación División

resultado = numero1 / numero2;
console.log(`El resultado de la división entre los dos valores es ${resultado}`);

//  Obtención del Módulo 

resultado = numero1 % numero2;
console.log(`El residuo es de la división entre los valores es ${resultado}`);


console.log(resultado);